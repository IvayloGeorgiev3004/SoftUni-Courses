function lastKNumbers(n, k) {
    // Създаваме начален масив с единствена стойност - 1
    let finalArray = new Array(1).fill(1);

    // Започваме цикъл от 1, защото първият елемент (1) вече е включен в масива
    for (let i = 1; i < n; i++) {
        let sum = 0; // Инициализираме променлива за сумата на последните k числа
        let start = Math.max(0, (i - k)); // Определяме началото на сумирането (не може да бъде по-малко от 0)

        // Цикъл за сумиране на последните k числа преди текущия индекс i
        for (let j = start; j < i; j++) {
            sum += finalArray[j]; // Сумираме числата в интервала [start, i)
        }

        // Добавяме сумата в края на масива
        finalArray.push(sum);
    }

    return finalArray; // Връщаме генерирания масив
}
console.log(lastKNumbers(6, 3)); // Изход: [1, 1, 2, 4, 7, 13]